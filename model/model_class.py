class Student:
    def __init__(self,ID,Name,Residence,DOB,Gender,Contact,Email):
        self.ID=ID
        self.Name=Name
        self.Residence=Residence
        self.DOB=DOB
        self.Gender=Gender
        self.Contact=Contact
        self.Email=Email
    def set_ID(self,ID):
        self.ID=ID
    def get_ID(self):
        return self.ID
    def set_Name(self,Name):
        self.Name=Name
    def get_Name(self):
        return self.Name
    def set_Residence(self,Residence):
        self.Residence=Residence
    def get_Residence(self):
        return self.Residence
    def set_DOB(self,DOB):
        self.DOB=DOB
    def get_DOB(self):
        return self.DOB
    def set_Gender(self,Gender):
        self.Gender=Gender
    def get_Gender(self):
        return self.Gender
    def set_Contact(self,Contact):
        self.Contact=Contact
    def get_Contact(self):
        return self.Contact
    def set_Email(self,Email):
        self.Email=Email
    def get_Email(self):
        return self.Email


